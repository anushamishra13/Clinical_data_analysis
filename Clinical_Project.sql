CREATE TABLE SAFETY_REVIEW AS
SELECT
    primaryid,
    drugname,
    pt,
    age,
    sex,

    CASE
        WHEN age >= 65 THEN 'HIGH RISK'
        ELSE 'STANDARD'
    END AS RISK_CATEGORY,

    CASE
        WHEN pt LIKE '%death%'
          OR pt LIKE '%failure%'
          OR pt LIKE '%toxicity%'
        THEN 'SERIOUS'

        ELSE 'NON-SERIOUS'
    END AS SERIOUSNESS

FROM RAW_DRUG_SAFETY;
SELECT *
FROM SAFETY_REVIEW;
SELECT SERIOUSNESS,
       COUNT(*) AS TOTAL_CASES
FROM SAFETY_REVIEW
GROUP BY SERIOUSNESS;
SELECT *
FROM SAFETY_REVIEW
WHERE RISK_CATEGORY = 'HIGH RISK';
