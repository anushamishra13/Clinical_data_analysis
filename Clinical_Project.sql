CREATE VIEW FINAL_SAFETY_RECONCILIATION AS

SELECT
    primaryid,
    drugname,
    pt,
    age,
    sex,

    CASE
        WHEN age IS NULL
          OR sex IS NULL
        THEN 'QUERY RAISED'

        ELSE 'VALIDATED'
    END AS DATA_STATUS,

    CASE
        WHEN pt LIKE '%death%'
          OR pt LIKE '%failure%'
          OR pt LIKE '%toxicity%'
        THEN 'SERIOUS AE'

        ELSE 'NON-SERIOUS AE'
    END AS SAFETY_CLASSIFICATION

FROM RAW_DRUG_SAFETY;
SELECT *
FROM FINAL_SAFETY_RECONCILIATION;
SELECT DATA_STATUS,
       SAFETY_CLASSIFICATION,
       COUNT(*) AS TOTAL_RECORDS
FROM FINAL_SAFETY_RECONCILIATION
GROUP BY DATA_STATUS, SAFETY_CLASSIFICATION
ORDER BY TOTAL_RECORDS DESC;
